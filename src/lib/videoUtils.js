export const getComplexColumns = (categoryId = 1) => {
  const colA = []; // spans 2
  const colB = []; // spans 1
  const colC = []; // spans 1

  let i = 0;
  let rowIndex = 0;
  
  const videos = VIDEOS_MAP[categoryId] || AI_VIDEOS;

  if (categoryId === 3) {
    const groups = videos.filter(v => v.isSlideshow);
    const singles = videos.filter(v => !v.isSlideshow);

    groups.forEach((g, idx) => {
      colA.push({ type: 'wide', id: `post-wide-${idx}`, card: g });
    });

    singles.forEach((s, idx) => {
      if (idx % 2 === 0) {
        colB.push({ type: 'single', id: `post-single-b-${idx}`, card: s });
      } else {
        colC.push({ type: 'single', id: `post-single-c-${idx}`, card: s });
      }
    });
  } else {
    while (i < videos.length) {
    if (rowIndex % 2 === 0) {
      // Even row: takes 4 videos
      const chunk = videos.slice(i, i + 4);
      if (chunk.length >= 2) {
        colA.push({ type: 'pair', id: `row-${rowIndex}-a`, pair: [chunk[0], chunk[1]] });
      } else if (chunk.length === 1) {
        // Fallback if odd number remains
        colA.push({ type: 'wide', id: `row-${rowIndex}-a`, card: chunk[0] });
      }
      
      if (chunk.length >= 3) {
        colB.push({ type: 'single', id: `row-${rowIndex}-b`, card: chunk[2] });
      }
      if (chunk.length >= 4) {
        colC.push({ type: 'single', id: `row-${rowIndex}-c`, card: chunk[3] });
      }
      i += 4;
    } else {
      // Odd row: takes 3 videos
      const chunk = videos.slice(i, i + 3);
      if (chunk.length >= 1) {
        colA.push({ type: 'wide', id: `row-${rowIndex}-a`, card: chunk[0] });
      }
      if (chunk.length >= 2) {
        colB.push({ type: 'single', id: `row-${rowIndex}-b`, card: chunk[1] });
      }
      if (chunk.length >= 3) {
        colC.push({ type: 'single', id: `row-${rowIndex}-c`, card: chunk[2] });
      }
      i += 3;
    }
    rowIndex++;
  }
  }

  return [
    { id: "col-a", speed: -0.8, offsetTop: 140, items: colA },       // Col 1: Slowest (wide column)
    { id: "col-b", speed: -2.5, offsetTop: 40, items: colB },        // Col 2: Medium-fast (middle column)
    { id: "col-c", speed: -1.5, offsetTop: 0, items: colC }          // Col 3: Medium (right column)
  ];
};

export function getYouTubeId(url) {
  if (!url) return "";
  const match = url.match(/embed\/([a-zA-Z0-9_-]+)/);
  return match ? match[1] : "";
}

export function getDriveId(url) {
  if (!url) return "";
  const match = url.match(/\/d\/([a-zA-Z0-9_-]+)/);
  return match ? match[1] : "";
}

export function getThumbnailUrl(video) {
  if (video.thumbnail) {
    if (video.thumbnail.includes("drive.google.com")) {
      const driveId = getDriveId(video.thumbnail);
      if (driveId) {
        return `https://drive.google.com/thumbnail?id=${driveId}&sz=w1000`;
      }
    }
    return video.thumbnail;
  }
  return `https://img.youtube.com/vi/${getYouTubeId(video.src)}/maxresdefault.jpg`;
}
